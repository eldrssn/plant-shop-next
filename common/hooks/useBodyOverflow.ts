'use client';

import { useEffect } from 'react';

export const useBodyOverflow = (isOpen: boolean) => {

  useEffect(() => {
    if (isOpen) {
      document.body.style.overflowY = 'hidden';
    } else {
      document.body.style.overflowY = 'auto';
    }

    return () => {
      document.body.style.overflowY = 'auto';
    };
  }, [isOpen]);
};
